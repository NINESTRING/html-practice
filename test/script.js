function linearTrend(input) {
  const n = input.length;
  const sigmax = input.reduce((prev, curr) => prev + curr[0], 0);
  const sigmay = input.reduce((prev, curr) => prev + curr[1], 0);
  const sigmaxx = input.reduce((prev, curr) => prev + curr[0] ** 2, 0);
  const sigmaxy = input.reduce((prev, curr) => prev + curr[0] * curr[1], 0);

  const slope = (n * sigmaxy - sigmax * sigmay) / (n * sigmaxx - sigmax ** 2);
  const offset = (sigmay - slope * sigmax) / n;

  return { slope, offset };
}
